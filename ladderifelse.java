class ladderifelse {
    public static void main(String a[]){
        int b = 10;
        if (b <20){
            System.out.println("b is less than 20");
        }
        else if (b > 9){
            System.out.println("b is greater than 9");
        }
        else if(b==10){
            System.out.println("b is equal to the 10");
        }
        else{
            System.out.println("Value not found");
        }
    }
}
