class Relation {
    public static void main(String a[]){
        int b = 4, d = 10;
        System.out.println("the value of b is : "+b+"and the value of d is: "+d);
        System.out.println(b==d);
        System.out.println(b!=d);
        System.out.println(b>d);
        System.out.println(b<d);
        System.out.println(b>=d);
        System.out.println(b<=d);

    }
}
